    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            Classroom ee = getModel();
            try {
                if (dao.add(ee) > 0) {
                    JOptionPane.showMessageDialog(this, "luu thanh cong");
                    fillDataTable();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Loi" + e.toString());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ban nhap day du thong tin");
        }
    }//GEN-LAST:event_btnSaveActionPerformed
    public void setModel(Classroom ee){
        txtID.setText(String.valueOf(ee.getId()));
        txtName.setText(ee.getName());
        txtMalop.setText(ee.getMalop());
        txtMota.setText(ee.getMota());
        txtSlmin.setText(String.valueOf(ee.getSlmin()));
        txtSlmax.setText(String.valueOf(ee.getSlmax()));
    }
    private void tblClassroomMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClassroomMouseClicked
        // TODO add your handling code here:
        int id = tblClassroom.rowAtPoint(evt.getPoint());
        id_Class = tblClassroom.getValueAt(id,0).toString();
        Classroom ee = dao.getClassroomByID(Integer.parseInt(id_Class));
        setModel(ee);
    }//GEN-LAST:event_tblClassroomMouseClicked

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        if(txtID.getText().isEmpty() || id_Class.length()==0){
            JOptionPane.showMessageDialog(this, "ban chua nhap id de xoa");
        }else{
            try {
                if(id_Class.length()>0){
                    dao.del(Integer.parseInt(id_Class));
                    
                }else{
                    dao.del(Integer.parseInt(txtID.getText()));
                    fillDataTable();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Loi:"+e.toString());
            }
        }
    }//GEN-LAST:event_btnDelActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        if(txtID.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "ban chua nhap id");
        }else{
            Classroom ee = dao.getClassroomByID(Integer.parseInt(txtID.getText()));
            setModel(ee);
        }
    }//GEN-LAST:event_btnFindActionPerformed
    public boolean validateForm() {
        if (txtID.getText().isEmpty() || txtName.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    public Classroom getModel() {
        Classroom ee = new Classroom();
        ee.setId(Integer.parseInt(txtID.getText()));
        ee.setName(txtName.getText());
        ee.setMalop(txtMalop.getText());
        ee.setMota(txtMota.getText());
        ee.setSlmin(Integer.parseInt(txtSlmin.getText()));
        ee.setSlmax(Integer.parseInt(txtSlmax.getText()));
        return ee;
    }

