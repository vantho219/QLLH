    public void fillDataTable() {
        DefaultTableModel model = (DefaultTableModel) tblClassroom.getModel();
        model.setRowCount(0);
        for(Classroom ee : dao.getAllClassroom()){
            Object dataRow[] = new Object[6];
            dataRow[0] = ee.getId();
            dataRow[1] = ee.getName();
            dataRow[2] = ee.getMalop();
            dataRow[3] = ee.getMota();
            dataRow[4] = ee.getSlmin();
            dataRow[5] = ee.getSlmax();
            model.addRow(dataRow);
        }
    }

