    public void fillDataTable() {
        DefaultTableModel model = (DefaultTableModel) tbStudent.getModel();
        model.setRowCount(0);
        for(Student ee : dao.getAllStudents()){
            Object dataRow[] = new Object[6];
            dataRow[0] = ee.getId();
            dataRow[1] = ee.getName();
            dataRow[2] = ee.getMalop();
            dataRow[3] = ee.getLophoc();
            dataRow[4] = ee.getSdt();
            dataRow[5] = ee.getMasv();
            model.addRow(dataRow);
        }
    }
