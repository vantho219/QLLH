    public Student getModel() {
        Student ee = new Student();
        ee.setId(Integer.parseInt(IDtxt.getText()));
        ee.setName(Nametxt.getText());
        ee.setMalop(Maloptxt.getText());
        ee.setLophoc(Lophoctxt.getText());
        ee.setSdt(Integer.parseInt(SDTtxt.getText()));
        ee.setMasv(Integer.parseInt(MaSVtxt.getText()));
        return ee;
    }
